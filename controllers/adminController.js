const drawPurposeSection = (doc, y) => {
  doc.fontSize(11).font('Helvetica-Bold').fillColor('#2d3748').text('PURPOSE OF REPORT', 50, y);
  doc.fontSize(9).font('Helvetica').fillColor('#4a5568')
    .text('This report provides analytical insights into quiz participation, user performance, and platform activity generated by the Quizzy Examination System.', 50, y + 15, { width: 500 });
  return y + 50;
};

const drawSummaryCards = (doc, y, stats) => {
  const cardWidth = 100;
  const cardGap = 20;
  let currentX = 50;

  stats.forEach(stat => {
    // Card Background
    doc.rect(currentX, y, cardWidth, 60).fill('#f8fafc').stroke('#e2e8f0');
    
    // Label
    doc.fontSize(8).font('Helvetica-Bold').fillColor('#718096').text(stat.label.toUpperCase(), currentX + 5, y + 10, { width: cardWidth - 10, align: 'center' });
    
    // Value
    doc.fontSize(14).font('Helvetica-Bold').fillColor('#1a365d').text(String(stat.value), currentX + 5, y + 30, { width: cardWidth - 10, align: 'center' });
    
    currentX += cardWidth + cardGap;
  });

  return y + 90;
};

const drawTableSection = (doc, y, headers, rows) => {
  const x = 50;
  const colWidths = [30, 130, 130, 60, 60, 100];
  
  // Header
  doc.rect(x, y, 510, 25).fill('#edf2f7');
  doc.fontSize(9).font('Helvetica-Bold').fillColor('#4a5568');
  
  let currentX = x;
  headers.forEach((h, i) => {
    doc.text(h, currentX + 5, y + 8, { width: colWidths[i] - 10 });
    currentX += colWidths[i];
  });

  y += 25;

  // Rows
  rows.forEach((row, rowIndex) => {
    if (y > 650) { // Threshold for footer/notice safety
      doc.addPage();
      y = 50;
      // Redraw header on new page
      doc.rect(x, y, 510, 25).fill('#edf2f7');
      doc.fontSize(9).font('Helvetica-Bold').fillColor('#4a5568');
      let tempX = x;
      headers.forEach((h, i) => {
        doc.text(h, tempX + 5, y + 8, { width: colWidths[i] - 10 });
        tempX += colWidths[i];
      });
      y += 25;
    }

    if (rowIndex % 2 === 1) {
      doc.rect(x, y, 510, 22).fill('#f7fafc');
    }
    
    doc.fontSize(8).font('Helvetica').fillColor('#2d3748');
    let rowX = x;
    row.forEach((cell, cellIndex) => {
      doc.text(String(cell), rowX + 5, y + 7, { width: colWidths[cellIndex] - 10, ellipsis: true });
      rowX += colWidths[cellIndex];
    });
    
    y += 22;
  });

  return y + 20;
};

