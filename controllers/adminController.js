    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename=attempt-result-${attempt._id}.pdf`);
    doc.pipe(res);

    buildPDF(doc, 'Quiz Attempt Result', `Generated on ${new Date().toLocaleDateString()}`);

    // Summary Section
    let y = 140;
    doc.fontSize(12).font('Helvetica-Bold').fillColor('#333').text('User Details:', 50, y);
    doc.fontSize(10).font('Helvetica').fillColor('#555')
      .text(`Name: ${attempt.userId?.name || 'N/A'}`, 50, y + 20)
      .text(`Email: ${attempt.userId?.email || 'N/A'}`, 50, y + 35);

    doc.fontSize(12).font('Helvetica-Bold').fillColor('#333').text('Quiz Summary:', 300, y);
